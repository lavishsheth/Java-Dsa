class RelationalOp {
public static void main(String args[]){
    int a=5;
    int b=2;

    if(a>b){
        System.out.println(a+" greater than "+b);
    }
    else{
        System.out.println("not true");
    }


    if(b<a){
        System.out.println(b+" less than "+a);
    }
    else{
        System.out.println("not true");
    }
}    
}
