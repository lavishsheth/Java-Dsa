class Op {
public static void main(String args[]){
    int a=5;
    int b=2;

    //greater than
    if(a>=b){
        System.out.println(a+" greater than equals to "+b);
    }
    else{
        System.out.println("not true");
    }

    //less than
    int c=2;
    int d=3;
    if(c<=d){
        System.out.println(c+" less than equals to "+d);
    }
    else{
        System.out.println("not true");
    }

    //equals
    if(c==d){
        System.out.println(c+" equals to "+d);
    }
    else{
        System.out.println("not true");
    }


    //Notequalsto
     int e=3;
     int f=4;
    if(e!=f){
        System.out.println(e+" Not equals to "+f);
   }
    else{
        System.out.println("not true");
    }
}    
}
